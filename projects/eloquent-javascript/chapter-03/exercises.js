////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  return Math.min(num1, num2);
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
  if(x % 2 === 0 || x == 0){ 
    return true; 
    } else if (x % 2 === 1 || x == -1){ 
      return false;
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let count = 0 
for (let i = 0; i < string.length; i++){ 
  if (string[i] == char){ 
    count += 1; 
  }
} return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0 
for (let i = 0; i < string.length; i++){ 
  if (string[i] == "B"){ 
    count += 1; 
  }
} return count; 
}

